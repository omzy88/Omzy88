package atomiko1;

public class Task {

	private int idtasks;
	private String task;
	
	public Task() {
		
	}

	public Task(String task) {

		this.task = task;

	}

	public int getIdtasks() {
		return this.idtasks;
	}

	public void setIdtasks(int idtasks) {
		this.idtasks = idtasks;
	}

	public String getTask() {
		return this.task;
	}

	public void setTask(String task) {
		this.task = task;
	}
	

}

